
public class Medicine{

   private int dosage;
   private String name;

   public Medicine(){
   
   }   
   public void SetName(String name){
      name = name;
   }
   
   public void SetDose(int dose){
      dosage = dose;
   }
   
   public String GetName(){
      return name;
   }
   
   public int GetDose(){
      return dosage;
   }
   
}   